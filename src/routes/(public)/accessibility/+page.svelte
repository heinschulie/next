	<h1>Accessibility policy</h1>
	<p><strong>The Promotion of Access to Information Act, 2 of 2000, (PAIA) provides you access to information that you might need to exercise or protect any rights.</strong></p>
	<p>Once you’ve met certain requirements, we’ll give you access to the information you need.</p>
	<p>We have compiled a manual in terms of PAIA to help you exercise your right to access the information.</p>
	<p>Click below to download our PAIA Manual, the prescribed forms and the relevant fees.</p>
